def generate_offer_letter(name, position, salary):
    return f"""
    -----------------------------
            OFFER LETTER
    -----------------------------

    Date: 01 March 2026

    Dear {name},

    We are pleased to offer you the position of {position} at our organization.

    Your compensation package will include a salary of {salary} per annum,
    along with other benefits as per company policy.

    We look forward to your valuable contribution to our team.

    Congratulations and welcome aboard!

    Sincerely,
    HR Department
    """